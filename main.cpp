void loop() {
  bool butt_state = !digitalRead(8); 

  if (butt_state) { 
    if (!buttonPressed) {  
      buttonPressTime = millis(); 	  
      buttonPressed = true; 
    } else {
      if (millis() - buttonPressTime >= 5000) { 
        if (!cumTriggered) {
          cum();
          cumTriggered = true;
        }
      } else {
        display.clearDisplay();
        jerk();
        display.display();
      }
    }
  } else {
    buttonPressed = false;
    buttonPressTime = 0; 
    cumTriggered = false;

    display.clearDisplay();
    display.drawBitmap(0, 0, dick1, 128, 64, SSD1306_WHITE);
    display.display();
  }
}